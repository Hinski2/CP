int solve(){
    string s; cin >> s;
    if(s.size() == 1) return 0;

    int ans;
    for(int i = s.size() - 1, ile = 0; i >= 0; i--, ile++){
        if(s[i] == '0') continue;
        int cnt = 0;
        for(int j = 0; j < i; j++){
            if(s[j] != '0') cnt++;
        }

        ans = ile + cnt;
        break;
    }

    return ans;
}

