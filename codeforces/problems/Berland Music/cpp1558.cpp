struct song{
    int likes;
    int ratting;
    int pos;
};

// does a should be before b
bool cmp(const song &a, const song &b){
    if(a.likes == b.likes)
        return a.ratting > b.ratting;
    return a.likes > b.likes;
}

void solve(){
    int n; cin >> n;
    vector<int> v(n); cin >> v;
    string l; cin >> l;

    vector<song> s(n);
    for(int i = 0; i < n; i++){
        s[i].likes = l[i] - '0';
        s[i].ratting = v[i];
        s[i].pos = i;
    }

    sort(all(s), cmp);
    vector<int> ans(n);
    int newl = n;
    for(int i = 0; i < n; i++)
        ans[s[i].pos] = newl--;
    cout << ans << endl;
}


int main(){
    io; int t; cin >> t;
    while(t--)
        solve();
}
